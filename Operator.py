# Assignment Operator
print(" the value of 1+2 is ", 1 + 2)

a = 32
a -= 5
print(a)

# Comparison Operators
b = (14 > 7)
c = (5 < 2)
print(b)
print(c)

# Logical Operators
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is", (bool1 and bool2))
print("The value of bool1 and bool2 is", (bool1 or bool2))
